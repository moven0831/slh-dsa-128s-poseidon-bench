/// <reference types="node" />
/**
 * Compile each Show benchmark configuration in `circom/benchmarks/configs.json`
 * and record the R1CS constraint count, wire count, and label count.
 *
 * Output: `circom/benchmarks/results/constraints.md` and `.csv`.
 *
 * Run from the `circom/` directory:
 *   npx ts-node scripts/benchmarks/run-constraints.ts
 */

import { execSync, spawnSync } from "child_process";
import { existsSync, mkdirSync, readFileSync, writeFileSync, rmSync } from "fs";
import { join, resolve } from "path";

interface BenchConfig {
  name: string;
  sweep: string;
  nClaims: number;
  maxPredicates: number;
  maxLogicTokens: number;
  expression: string[];
  predicateOp: "le" | "ge" | "eq";
  rhsIsRef: boolean;
}

interface ConfigsFile {
  comment: string;
  valueBits: number;
  configs: BenchConfig[];
}

interface R1CSInfo {
  wires: number;
  constraints: number;
  privateInputs: number;
  publicInputs: number;
  publicOutputs: number;
  labels: number;
}

interface BenchResult extends R1CSInfo {
  config: BenchConfig;
}

const REPO_CIRCOM_DIR = resolve(__dirname, "..", "..");
const CONFIGS_PATH = join(REPO_CIRCOM_DIR, "benchmarks", "configs.json");
const RESULTS_DIR = join(REPO_CIRCOM_DIR, "benchmarks", "results");
const BENCH_BUILD_DIR = join(REPO_CIRCOM_DIR, "build", "_bench");
const BENCH_MAIN_DIR = join(REPO_CIRCOM_DIR, "circuits", "_bench_main");

function writeMainFile(config: BenchConfig, valueBits: number): string {
  if (!existsSync(BENCH_MAIN_DIR)) mkdirSync(BENCH_MAIN_DIR, { recursive: true });
  const mainPath = join(BENCH_MAIN_DIR, `${config.name}.circom`);
  const content = `pragma circom 2.2.3;

include "../show.circom";

component main {public[deviceKeyX, deviceKeyY]} = Show(${config.nClaims}, ${config.maxPredicates}, ${config.maxLogicTokens}, ${valueBits});
`;
  writeFileSync(mainPath, content);
  return mainPath;
}

function compileR1csOnly(mainPath: string, config: BenchConfig): string {
  const outDir = join(BENCH_BUILD_DIR, config.name);
  if (existsSync(outDir)) rmSync(outDir, { recursive: true, force: true });
  mkdirSync(outDir, { recursive: true });

  const cmd = "circom";
  const args = [
    mainPath,
    "--r1cs",
    "--sym",
    "-p", "secq256r1",
    "--O2",
    "-l", join(REPO_CIRCOM_DIR, "circuits"),
    "-l", join(REPO_CIRCOM_DIR, "node_modules"),
    "-o", outDir,
  ];

  const res = spawnSync(cmd, args, { encoding: "utf8" });
  if (res.status !== 0) {
    throw new Error(`circom failed for ${config.name}:\nstdout: ${res.stdout}\nstderr: ${res.stderr}`);
  }
  return join(outDir, `${config.name}.r1cs`);
}

function r1csInfo(r1csPath: string): R1CSInfo {
  const out = execSync(`npx snarkjs r1cs info ${r1csPath}`, { encoding: "utf8" });
  const grab = (label: string): number => {
    const re = new RegExp(`# of ${label}:\\s*(\\d+)`);
    const m = out.match(re);
    if (!m) throw new Error(`Could not parse "${label}" from snarkjs output:\n${out}`);
    return Number(m[1]);
  };
  return {
    wires: grab("Wires"),
    constraints: grab("Constraints"),
    privateInputs: grab("Private Inputs"),
    publicInputs: grab("Public Inputs"),
    publicOutputs: grab("Outputs"),
    labels: grab("Labels"),
  };
}

function exprToString(expr: string[]): string {
  return expr.join(" ");
}

function renderMarkdown(results: BenchResult[]): string {
  const groups = new Map<string, BenchResult[]>();
  for (const r of results) {
    if (!groups.has(r.config.sweep)) groups.set(r.config.sweep, []);
    groups.get(r.config.sweep)!.push(r);
  }

  const lines: string[] = [];
  lines.push("# Show circuit: constraint counts");
  lines.push("");
  lines.push("Generated by `scripts/benchmarks/run-constraints.ts`. Each row is a separate compilation of `Show(nClaims, maxPredicates, maxLogicTokens, valueBits)` with `valueBits = 64`. The `predicateLen` and `exprLen` runtime inputs equal `maxPredicates` and `maxLogicTokens` respectively, so the circuit is sized exactly to its workload.");
  lines.push("");

  for (const [sweep, rows] of groups) {
    lines.push(`## Sweep: \`${sweep}\``);
    lines.push("");
    lines.push("| name | nClaims | maxPredicates (m) | maxLogicTokens (t) | predicateOp | rhsIsRef | expression | constraints | wires | labels |");
    lines.push("| --- | ---: | ---: | ---: | --- | :---: | --- | ---: | ---: | ---: |");
    for (const r of rows) {
      const c = r.config;
      lines.push(
        `| ${c.name} | ${c.nClaims} | ${c.maxPredicates} | ${c.maxLogicTokens} | ${c.predicateOp} | ${c.rhsIsRef ? "yes" : "no"} | \`${exprToString(c.expression)}\` | ${r.constraints.toLocaleString()} | ${r.wires.toLocaleString()} | ${r.labels.toLocaleString()} |`
      );
    }
    lines.push("");
  }

  return lines.join("\n");
}

function main() {
  if (!existsSync(CONFIGS_PATH)) {
    throw new Error(`configs file not found: ${CONFIGS_PATH}`);
  }
  if (!existsSync(RESULTS_DIR)) mkdirSync(RESULTS_DIR, { recursive: true });

  const file: ConfigsFile = JSON.parse(readFileSync(CONFIGS_PATH, "utf8"));
  const results: BenchResult[] = [];

  for (const config of file.configs) {
    const tag = `[${config.name}] n=${config.nClaims} m=${config.maxPredicates} t=${config.maxLogicTokens}`;
    process.stdout.write(`${tag} ... compiling ... `);
    const mainPath = writeMainFile(config, file.valueBits);
    const r1cs = compileR1csOnly(mainPath, config);
    const info = r1csInfo(r1cs);
    process.stdout.write(`${info.constraints.toLocaleString()} constraints, ${info.wires.toLocaleString()} wires\n`);
    results.push({ config, ...info });
  }

  const md = renderMarkdown(results);
  const mdPath = join(RESULTS_DIR, "constraints.md");
  writeFileSync(mdPath, md);

  // Machine-readable intermediate for the report generator.
  const jsonPath = join(RESULTS_DIR, "constraints-data.json");
  writeFileSync(jsonPath, JSON.stringify(results, null, 2));

  console.log("");
  console.log(md);
  console.log("");
  console.log(`Wrote ${mdPath}`);
  console.log(`Wrote ${jsonPath}`);
}

main();
